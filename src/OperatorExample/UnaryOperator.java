
package OperatorExample;


public class UnaryOperator {
    
    public static void main(String[] args) {
        int a = -10;
        int b = -a;
        System.out.println("The b is\t"+ b);
        
        int x, result;
        x= 10;
        result= -x;//Operand with unary operator.(minus)//-10
        System.out.println("The value of x: \t"+ result);
        
        result= +x;//Operand with unary operator.(plus)
        System.out.println("The value of x: \t"+ result);
        result= -x;//Operand with unary operator.
        System.out.println("The value of x: \t"+ result);
        
        
        
        
        
        
    }
}
