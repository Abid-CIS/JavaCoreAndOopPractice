
package ForeachLoop;


public class EnhancedForLoopDemo {
    
    public static void main(String[] args) {
        String [] names = {"Apurba", "Shuvra", "Rothosri", "Shama","Rothi", "Sribash"};
        for (String nam : names) {
            System.out.print(nam + "\t");
        }
        System.out.println();
    }
}
